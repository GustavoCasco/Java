package POO.exerGenerico;

public class calculadora {
    public int soma(int a, int b )
    {
        System.out.println("A soma desse numeros inteiros é: ");
        return a + b;
    }
    public long soma(long a, long b )
    {
        System.out.println("A soma desse numero LONG é: ");
        return a + b;
    }
    public float soma(Float a, Float b )
    {
        System.out.println("A soma desse numero FLOAT é: ");
        return a + b;
    }
    public double soma(Double a, Double b )
    {
        System.out.println("A soma desse numero DOUBLE é: ");

        return a + b;
    }
}
