package simpleprograms;

public class tipoVariavelDouble {
    public static void main(String[] args) {
        //Iremos trabalhar com double para casas flutuantes
        //Declarando variavel ja passando o valor
        Double salario =  1250.70;

        //Exibindo o valor dessa variavel com sysOut concatenado
        System.out.println("O salario é de: " + salario);

        //Assim como o tipo INT. é possivel fazer operações matematicas como por exemplo:
        Double divisao =  3.14/2;
        System.out.println("O resultado dessa divisão é de: " + divisao);

        double idade = 30.0;
        System.out.println("A idade é de: " + (int)idade);
    }
}
