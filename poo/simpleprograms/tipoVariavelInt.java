package simpleprograms;

public class tipoVariavelInt {
   public static void main(String[] args) {
       //Como declarar uma variavel
       int idade;
       //Setando valor a variavel
       idade = 37;

       //SysOut mostrando o valor da variavel
       System.out.println(idade);

       //A idade é o resultado desse calculo
       idade = (30 + 10);
       //Mostrando que é possivel diversos tipo de operação em uma unica conta
       idade = ((7*5) + 2);

       //Formas de exibir concatenado
       System.out.println("A idade é: " + idade);
   } 
}
